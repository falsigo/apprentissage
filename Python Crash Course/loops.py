# For loop

for letter in "Draft the car to me":
    print(letter)

friends = ["Kim", "Larry", "Karen"]
for friend in friends:
    print(friend)

for index in range(3, 10):
    print(index)
for dex in range(10):
    print(dex)

for index in range(len(friends)):
    print(friends[index])